package tema4;

public class T4PruebasSentSelec {
    public static void main(String[] args) {
        int valor = 100;

        // Sentencia de seleccion simple
        if (valor >= 100) {
            System.out.println("El valor es igual o mayor que 100");
        } else {
            System.out.println("El valor es menor que 100");
        }
    }

}
